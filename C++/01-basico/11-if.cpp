#include <iostream>

using namespace std;

int main() {

    // Declaración variable local
   int a = 10;
 
   // Evaluar la condición booleana
   if( a < 20 ) {
      // Si la condición se cumple (es verdadero) imprime lo siguiente
      cout << "a es menor a 20;" << endl;
   }
   cout << "El valor de a es : " << a << endl;
 
   return 0;

}