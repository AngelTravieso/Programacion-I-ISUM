// * 01 - Sintaxis Básica de C++

// El lenguaje C++ define varios encabezados, que contienen información necesaria o útil para su programa. Para este programa, se necesita el encabezado <iostream>.
#include <iostream>

// La línea que usa el espacio de nombres std; le dice al compilador que use el espacio de nombres estándar. Los espacios de nombres son una adición relativamente reciente a C++.
using namespace std;

// main() es donde la ejecución del programa comienza
int main() {

    cout << "Hola Mundo desde C++" << endl; // Imprime "Hola Mundo desde C++, endl inserta una nueva linea (equivalente a \n)"

    // La siguiente línea devuelve 0; finaliza la función principal () y hace que devuelva el valor 0 al proceso de llamada.
    return 0;
}

